st.markdown("""
このアプリは、大学生が英語学習において自己調整学習（SRL）を実践するために開発されました。  
毎日の学習記録・目標設定・振り返り・進捗の可視化を通じて、より効果的な学習が可能になります。

---

### 📋 利用できるページ（左のサイドバーから選択）：

- 📝 **記録入力**：学習時間・内容・感想を記録（学生向け）  
- 🔄 **目標と記録の比較**：ビジョンとログを比較し内省を促進（学生向け）  
- 🌟 **ビジョン設定**：英語学習の目標・理由・方法を記入（学生向け）  
- 📈 **進捗グラフ**：1週間の学習時間をグラフで確認（学生・教員共通）  
- 🗣️ **フィードバック記録**：教員から学生へのコメントを記録（教員専用）

---

> ※ このアプリは東京経済大学のゼミ活動の一環として使用されています。
>  
> ※ 「フィードバック記録」は教員のみが記入するページです。学生の入力は不要です。
""")

